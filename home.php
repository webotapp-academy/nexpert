    <script>
    // Typing effect for search placeholder
    const placeholderTexts = [
        'e.g. Career Change',
        'e.g. Parenting Advice',
        'e.g. Fitness Coaching',
        'e.g. Business Strategy',
        'e.g. Language Learning',
        'e.g. Personal Finance'
    ];
    
    let currentTextIndex = 0;
    let currentCharIndex = 0;
    let isDeleting = false;
    let typingSpeed = 100;
    
    function typeEffect() {
        const searchInput = document.getElementById('searchInput');
        if (!searchInput || searchInput === document.activeElement) return;
        
        const currentText = placeholderTexts[currentTextIndex];
        
        if (isDeleting) {
            searchInput.placeholder = currentText.substring(0, currentCharIndex - 1);
            currentCharIndex--;
            typingSpeed = 50;
        } else {
            searchInput.placeholder = currentText.substring(0, currentCharIndex + 1);
            currentCharIndex++;
            typingSpeed = 100;
        }
        
        if (!isDeleting && currentCharIndex === currentText.length) {
            isDeleting = true;
            typingSpeed = 2000;
        } else if (isDeleting && currentCharIndex === 0) {
            isDeleting = false;
            currentTextIndex = (currentTextIndex + 1) % placeholderTexts.length;
            typingSpeed = 500;
        }
        
        setTimeout(typeEffect, typingSpeed);
    }
    
    // Start typing effect when page loads
    document.addEventListener('DOMContentLoaded', function() {
        setTimeout(typeEffect, 1000);
    });

    // Expert search functionality
    document.getElementById('expertSearchForm').addEventListener('submit', function(e) {
        e.preventDefault();
        const query = document.getElementById('searchInput').value.trim();
        if (query) {
            window.location.href = `?panel=learner&page=browse-experts&search=${encodeURIComponent(query)}`;
        }
    });

    function searchForExpertise(query) {
        document.getElementById('searchInput').value = query;
        window.location.href = `?panel=learner&page=browse-experts&search=${encodeURIComponent(query)}`;
    }

    // Load featured experts from database
    async function loadFeaturedExperts() {
        try {
            const response = await fetch('/admin-panel/apis/learner/browse-experts.php?sort_by=latest&limit=6');
            const result = await response.json();
            
            if (result.success && result.data.length > 0) {
                const grid = document.getElementById('featured-experts-grid');
                grid.innerHTML = '';
                
                result.data.slice(0, 6).forEach(expert => {
                    const skills = Array.isArray(expert.skills) ? expert.skills : (expert.skills ? expert.skills.split(',').map(s => s.trim()) : []);
                    const skillColors = ['blue', 'purple', 'orange', 'indigo', 'emerald', 'pink'];
                    const randomColor = skillColors[Math.floor(Math.random() * skillColors.length)];
                    
                    const stars = '★'.repeat(Math.floor(expert.avg_rating || 5)) + '☆'.repeat(5 - Math.floor(expert.avg_rating || 5));
                    
                    const expertCard = `
                        <div class="bg-white rounded-xl shadow-lg overflow-hidden hover:shadow-xl transition duration-300 border">
                            <div class="p-6">
                                <div class="flex items-center mb-4">
                                    <img src="${expert.profile_photo || 'https://ui-avatars.com/api/?name=' + encodeURIComponent(expert.name) + '&background=3B82F6&color=fff&size=200'}" 
                                         alt="${escapeHtml(expert.name)}" 
                                         class="w-20 h-20 rounded-full mr-4 object-cover">
                                    <div>
                                        <h4 class="text-xl font-semibold text-gray-800">${escapeHtml(expert.name)}</h4>
                                        <p class="text-gray-600 text-sm">${escapeHtml(expert.professional_title || expert.expert_type || 'Expert')}</p>
                                        <div class="flex items-center mt-1">
                                            <div class="flex text-yellow-400 text-sm">${stars}</div>
                                            <span class="text-gray-500 text-xs ml-1">(${expert.avg_rating || '5.0'}) • ${expert.review_count || expert.total_reviews || 0} reviews</span>
                                        </div>
                                    </div>
                                </div>
                                <p class="text-gray-700 text-sm mb-4 line-clamp-2">${escapeHtml(expert.bio || expert.tagline || 'Expert professional')}</p>
                                <div class="flex flex-wrap gap-1 mb-4">
                                    ${skills.slice(0, 3).map(skill => 
                                        `<span class="px-2 py-1 bg-${randomColor}-100 text-${randomColor}-800 text-xs rounded-full">${escapeHtml(skill)}</span>`
                                    ).join('')}
                                </div>
                                <div class="flex justify-between items-center">
                                    <span class="text-lg font-semibold text-primary">₹${expert.hourly_rate || '0'}/hour</span>
                                    <a href="?panel=learner&page=expert-profile&expert_id=${expert.id}" class="bg-primary text-white px-4 py-2 rounded-lg hover:bg-secondary transition text-sm">
                                        View Profile
                                    </a>
                                </div>
                            </div>
                        </div>
                    `;
                    
                    grid.innerHTML += expertCard;
                });
            } else {
                document.getElementById('featured-experts-grid').innerHTML = '<div class="col-span-full text-center py-8"><p class="text-gray-600">No experts available at the moment.</p></div>';
            }
        } catch (error) {
            console.error('Error loading featured experts:', error);
            document.getElementById('featured-experts-grid').innerHTML = '<div class="col-span-full text-center py-8"><p class="text-gray-600">Error loading experts. Please try again later.</p></div>';
        }
    }

    // Escape HTML to prevent XSS
    function escapeHtml(text) {
        const div = document.createElement('div');
        div.textContent = text;
        return div.innerHTML;
    }

    // Load featured experts on page load
    document.addEventListener('DOMContentLoaded', loadFeaturedExperts);
    </script>
